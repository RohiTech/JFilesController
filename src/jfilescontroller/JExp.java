    public static void escribirFichero()
    {
        String p;

        p = pwd;

        try
        {
            escribir.writeUTF(p);
            escribir.close();
        }
        catch(IOException io)
        {
            System.out.println("Error");
        }
    }

    public static void Abrir_Pwd()
    {
        Fichero = new File("jpwd.exe");

        if(!Fichero.exists())
        {
             JOptionPane.showMessageDialog(null,"Error, el programa necesita el archivo jpwd.exe","Error",JOptionPane.ERROR_MESSAGE);
             System.exit(0);
        }
        else
        {
            try
            {
                leer = new DataInputStream(new FileInputStream(Fichero));
                leerFichero();
            }
            catch(Exception e)
            {
            }
        }
    }

    public static void leerFichero()
    {
        String p;
        // abrir archivo
        try
        {
            p = leer.readUTF();
            pwd = p;
            leer.close();
        }
        catch(Exception e)
        {
            System.out.println("Error");
        }
    }

