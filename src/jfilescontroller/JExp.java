public static void Cambiar_Pwd()
{
    if(jPasswordField1.getText().equals(jPasswordField2.getText()))
    {
        leerFichero();

        if(jPasswordField1.getText().equalsIgnoreCase(pwd))
        {
            pwd = jPasswordField3.getText();
            Guardar_Pwd();
            JOptionPane.showMessageDialog(null, "La contraseña se ha cambiado satisfactoriamente", "JFilesController", JOptionPane.INFORMATION_MESSAGE);
            Principal.j.setVisible(false);
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Contraseña incorrecta!", "Error", JOptionPane.ERROR_MESSAGE);
            Limpiar();
        }
    }
    else
    {
        JOptionPane.showMessageDialog(null, "Error al digitar su contraseña", "JFilesController", JOptionPane.ERROR_MESSAGE);
        Limpiar();
    }
}

